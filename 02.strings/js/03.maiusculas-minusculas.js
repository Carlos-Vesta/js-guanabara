/**
 *Para converter uma String para maiúsculas ou minúsculas,
 usamos as funções "toLowerCase()" para minúsculas e "toUpperCase()"
 para maiúsculas.
 */


const texto = "Olá Javascript";


console.log(texto.toLocaleLowerCase()); // olá javascript
console.log(texto.toUpperCase()); // OLÁ JAVASCRIPT

